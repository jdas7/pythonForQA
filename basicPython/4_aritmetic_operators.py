from math import pi


num1 = 3
num2 = 2
# sum
suma = num1 + num2
print(f'Result suma: {suma} ')
# subtract
resta = num1 - num2
print(f'Result resta: {resta} ')
# multiplication
multiplication = num1 * num2
print(f'Result multiplication: {multiplication}')
# division
division = num1 / num2
print(f'Result division: {division}')
division = num1 // num2
print(f'Result division (int): {division}')
# module
module = num1 % num2
print(f'Result residue division (module): {module}')
# exponent
exponent = num1 ** num2
print(f'Result exponent: {exponent}')

x = pi
print(f'the number pi is: {x}')
